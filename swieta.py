ax.add_patch(patches.Circle((5, 7.0), 0.6, color="white"))       # Broda
ax.add_patch(patches.Polygon([[4.2, 8], [5, 9.3], [5.8, 8]], color="red")) # Czapka
ax.add_patch(patches.Circle((5, 9.3), 0.25, color="white"))      # Pompon

# Rƒôce uniesione do g√≥ry
ax.add_patch(patches.Rectangle((3.2, 7.2), 0.8, 1.8, color="red"))  # lewa rƒôka
